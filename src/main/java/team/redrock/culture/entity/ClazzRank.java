package team.redrock.culture.entity;

public class ClazzRank {
    private int clazzID;
    private int clazzAmount;
    private int rank;

    public int getClazzID() {
        return clazzID;
    }

    public void setClazzID(int clazzID) {
        this.clazzID = clazzID;
    }

    public int getClazzAmount() {
        return clazzAmount;
    }

    public void setClazzAmount(int clazzAmount) {
        this.clazzAmount = clazzAmount;
    }

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }
}
